"""Moduł zawierający podstawowe operacje matematyczne kalkulatora."""


def add(a: float, b: float) -> float:
    """Zwraca sumę dwóch liczb zmiennoprzecinkowych.

    Args:
        a (float): Pierwsza liczba.
        b (float): Druga liczba.

    Returns:
        float: Wynik dodawania.
    """
    return a + b


def subtract(a: float, b: float) -> float:
    """Zwraca różnicę dwóch liczb zmiennoprzecinkowych.

    Args:
        a (float): Pierwsza liczba.
        b (float): Druga liczba.

    Returns:
        float: Wynik odejmowania.
    """
    return a - b
